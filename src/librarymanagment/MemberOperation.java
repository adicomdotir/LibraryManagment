    private void btnSearch2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSearch2MouseClicked
        String command = "SELECT * FROM member WHERE id=" + Integer.valueOf(jTextField1.getText()) + ";";
        String str = SqlHelper.select(command);
        String[] strArr = str.split("&");
        txtCode.setText(strArr[0]);
        txtCode.setEnabled(false);
        txtNationalId.setText(strArr[1]);
        cbSex.setSelectedItem(strArr[2]);
        txtFName.setText(strArr[3]);
        txtLName.setText(strArr[4]);
        txtPhone.setText(strArr[5]);
        txtAddress.setText(strArr[6]);
    }//GEN-LAST:event_btnSearch2MouseClicked

    private void btnEditMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEditMouseClicked
        String sql = "UPDATE member set n_code=" + Integer.valueOf(txtNationalId.getText())
                    + ",sex='" + (String) cbSex.getSelectedItem() 
                    + "',f_name='" + txtFName.getText()
                    + "',l_name='" + txtLName.getText()
                    + "',phone='" + txtPhone.getText()
                    + "',address='" + txtAddress.getText()
                    + "' where id=" + Integer.valueOf(txtCode.getText()) + ";";
        SqlHelper.update(sql);
        clearComponent();
    }//GEN-LAST:event_btnEditMouseClicked

